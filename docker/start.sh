#!/bin/sh
# ---------------------------------------------------------------------------------------------------------------------
root=$1
cd $root
# ---------------------------------------------------------------------------------------------------------------------
# Build Migrations Scripts.
echo "<------------------- makemigrations --------------------->"
./manage.py makemigrations skillprofil --settings kickstarter.settings
# ---------------------------------------------------------------------------------------------------------------------
# Execute Migration.
echo "<------------------- migrate ---------------------------->"
./manage.py migrate --settings kickstarter.settings
# ---------------------------------------------------------------------------------------------------------------------
# Run the server.
echo "<------------------- runserver -------------------------->"
./manage.py runserver 0.0.0.0:8000 --settings kickstarter.settings
# ---------------------------------------------------------------------------------------------------------------------
